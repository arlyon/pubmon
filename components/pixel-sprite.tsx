interface PixelSpriteProps {
	name: string;
	size?: number;
	className?: string;
	flipped?: boolean;
	animated?: boolean;
}

export function PixelSprite({
	name,
	size = 6,
	className = "",
	flipped = false,
	animated = false,
}: PixelSpriteProps) {
	const spriteKey = name.toLowerCase();
	const sprite = SPRITE_DATA[spriteKey];

	if (!sprite) {
		return (
			<div
				className={`${className}`}
				style={{ width: size * 10, height: size * 10, background: "#333" }}
			/>
		);
	}

	return (
		<div
			className={`${className}`}
			style={{
				width: size * 10,
				height: size * 10,
				transform: flipped ? "scaleX(-1)" : undefined,
				animation: animated
					? "pixel-bounce 1s ease-in-out infinite"
					: undefined,
			}}
		>
			<svg
				viewBox="0 0 10 10"
				width={size * 10}
				height={size * 10}
				style={{ imageRendering: "pixelated" }}
			>
				{sprite.pixels.map((row, y) =>
					row.map((cell, x) => {
						if (cell === ".") return null;
						const color = sprite.palette[cell] || "#ff00ff";
						return (
							<rect
								key={`${x}-${y}`}
								x={x}
								y={y}
								width={1}
								height={1}
								fill={color}
							/>
						);
					}),
				)}
			</svg>
		</div>
	);
}

